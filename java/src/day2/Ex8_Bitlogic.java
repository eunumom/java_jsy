package day2;

public class Ex8_Bitlogic {

	public static void main(String[] args) {
		byte no1 = 3;
		byte no2 = 10;
		System.out.println(no1 & no2);
		System.out.println(no1 | no2);
		System.out.println(no1 ^ no2);
		System.out.println(~ no1);
		System.out.println(~ no2);
		// 비트 논리는 계산 했을 때 특정 값이 나옴

	}

}
