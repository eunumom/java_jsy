package day2;

public class Ex5_Comparison {

	public static void main(String[] args) {
		/* >= 또는 <= 순서를 지켜서 써야 함
		 * == 를 = 과 혼동하지 않기!
		 * 결과 값이 참 또는 거짓임
		 */
		int score = 95;
		System.out.println(score > 95);
		System.out.println(score >= 95);
		System.out.println(score < 95);
		System.out.println(score <= 95);
		System.out.println(score < 95);
		System.out.println(score == 95);
		System.out.println(score != 95);
		

	}

}
